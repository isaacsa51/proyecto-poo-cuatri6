    private void btnPelisDisponiblesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPelisDisponiblesActionPerformed
        //Mostrar en la lista las peliculas disponibles
        try{
            String qMostrarPelis = "SELECT * FROM peliculas WHERE stock >= 0";

            DefaultListModel listaPelis = new DefaultListModel();
            
            PreparedStatement execQuery = conStatus.prepareStatement(qMostrarPelis);
            ResultSet resQuery = execQuery.executeQuery();

            //Ir atraves de la tabla e insertar la información
            while (resQuery.next()){
                String peliNombre = resQuery.getString("nombre"); //get the element in column "item_code"
                listaPelis.addElement(peliNombre); //add each item to the model
            }
            lstPeliculas.setModel(listaPelis);
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }finally{
            if(bdcon != null)
                bdcon.desconectarBD();
        }
    }//GEN-LAST:event_btnPelisDisponiblesActionPerformed

